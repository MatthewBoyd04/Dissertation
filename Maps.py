# ── Assembled maps ─────────────────────────────────────────────────────────
# map_15x15  : Panel A only
# map_30x30  : rows 0-14 = A|B, rows 15-29 = C|D
# map_45x45  : rows 0-14 = A|B|C, rows 15-29 = D|A|B, rows 30-44 = C|D|A
#   → every 15×15 quadrant has a different panel; no block tiles exactly.

map_15x15 = [row[:] for row in _A]

map_30x30 = (
    [_A[r] + _B[r] for r in range(15)] +
    [_C[r] + _D[r] for r in range(15)]
)

map_45x45 = (
    [_A[r] + _B[r] + _C[r] for r in range(15)] +
    [_D[r] + _A[r] + _B[r] for r in range(15)] +
    [_C[r] + _D[r] + _A[r] for r in range(15)]
)

if isSafe:
    for _map in (map_15x15, map_30x30, map_45x45):
        for _row in _map:
            for _j in range(len(_row)):
                if _row[_j] == 2:
                    _row[_j] = 0
